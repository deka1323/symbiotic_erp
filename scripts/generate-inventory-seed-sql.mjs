import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))

const repoDefaultJson = path.join(__dirname, 'inventory_data.json')
const downloadsFallback = 'C:\\Users\\GCU ERP\\Downloads\\inventory_data.json'
const defaultJson = fs.existsSync(repoDefaultJson) ? repoDefaultJson : downloadsFallback

const argv = process.argv.slice(2)
const flags = new Set(argv.filter((a) => a.startsWith('--')))
const positional = argv.filter((a) => !a.startsWith('--'))
const addToExistingStock = flags.has('--add-to-existing-stock')
const resetMissingToZero = flags.has('--reset-missing-to-zero')

const inventoryFlag = [...flags].find((f) => f.startsWith('--inventory-id='))
const invId = inventoryFlag
  ? inventoryFlag.slice('--inventory-id='.length)
  : '6ced05cf-db66-4b69-8aff-61217300b235'

const jsonPath = path.resolve(positional[0] || defaultJson)
const sourceLabel = path.basename(jsonPath)
const outPath =
  positional[1] ||
  path.join(__dirname, path.basename(jsonPath, path.extname(jsonPath)) + '_seed.sql')

const MONTH_CODES = ['A', 'B', 'C', 'D', 'E', 'F', 'G', 'H', 'I', 'J', 'K', 'L']

function parseBatchId(batchId) {
  const m = String(batchId).match(/^(\d{2})\/SFPL\/([A-L])(\d{2})$/)
  if (!m) throw new Error(`Invalid batchId: ${batchId}`)
  const yy = parseInt(m[1], 10)
  const mi = MONTH_CODES.indexOf(m[2])
  const rawDd = parseInt(m[3], 10)
  const dd = rawDd === 0 ? 1 : rawDd
  const year = 2000 + yy
  const d = new Date(Date.UTC(year, mi, dd))
  if (d.getUTCFullYear() !== year || d.getUTCMonth() !== mi || d.getUTCDate() !== dd) {
    throw new Error(`Bad calendar date for ${batchId}`)
  }
  const iso = `${year}-${String(mi + 1).padStart(2, '0')}-${String(dd).padStart(2, '0')}`
  return { productionDay: iso, batchId, normalizedDayFrom00: rawDd === 0 }
}

function escSql(str) {
  return String(str).replace(/'/g, "''")
}

const rawRows = JSON.parse(fs.readFileSync(jsonPath, 'utf8'))
const grouped = new Map()
for (const r of rawRows) {
  const key = `${r.name}|||${r.batchId}`
  const qty = parseInt(r.amount, 10)
  if (Number.isNaN(qty)) throw new Error(`Bad amount for ${r.name}`)
  grouped.set(key, (grouped.get(key) || 0) + qty)
}
const rows = [...grouped.entries()].map(([key, quantity]) => {
  const [name, batchId] = key.split('|||')
  return { name, batchId, amount: String(quantity) }
})
const uniqueBatchIds = [...new Set(rows.map((r) => r.batchId))]

const parsedBatchById = new Map(uniqueBatchIds.map((bid) => [bid, parseBatchId(bid)]))
const hasNormalizedDayFrom00 = [...parsedBatchById.values()].some((p) => p.normalizedDayFrom00)

const out = []
out.push(`-- Generated by scripts/generate-inventory-seed-sql.mjs from ${sourceLabel}`)
out.push(`-- Inventory: ${invId}`)
if (addToExistingStock) {
  out.push('-- Stock: ON CONFLICT adds JSON amount to existing quantity (--add-to-existing-stock)')
} else {
  out.push('-- Stock: ON CONFLICT replaces quantity with JSON amount (default)')
}
if (hasNormalizedDayFrom00) {
  out.push('-- Note: batch IDs ending with day 00 were normalized to day 01 for production_day.')
}
if (resetMissingToZero) {
  out.push('-- Reset mode: existing stock rows in this inventory not present in input are set to quantity 0.')
}
out.push('BEGIN;')
out.push('')
out.push(
  '-- Batches: production_day from batchId; production_date = CURRENT_TIMESTAMP. ' +
    'If (inventory_id, batchId) already exists, INSERT is skipped (no duplicate batch).'
)
for (const bid of uniqueBatchIds) {
  const { productionDay } = parsedBatchById.get(bid)
  out.push('INSERT INTO "batches" (id, "batchId", inventory_id, production_date, production_day, remark)')
  out.push('VALUES (')
  out.push('  gen_random_uuid()::text,')
  out.push(`  '${escSql(bid)}',`)
  out.push(`  '${invId}',`)
  out.push('  CURRENT_TIMESTAMP,')
  out.push(`  DATE '${productionDay}',`)
  out.push(`  'Seeded from ${escSql(sourceLabel)}'`)
  out.push(')')
  out.push('ON CONFLICT ("inventory_id", "batchId") DO NOTHING;')
  out.push('')
}

out.push('-- Stocks: sku by exact name; batch resolved by inventory + batchId')
const stockInsertTable = addToExistingStock ? 'INSERT INTO "stocks" AS st' : 'INSERT INTO "stocks"'
const conflictUpdate = addToExistingStock
  ? 'DO UPDATE SET quantity = st.quantity + EXCLUDED.quantity, last_updated = NOW();'
  : 'DO UPDATE SET quantity = EXCLUDED.quantity, last_updated = NOW();'

for (const r of rows) {
  const qty = parseInt(r.amount, 10)
  if (Number.isNaN(qty)) throw new Error(`Bad amount for ${r.name}`)
  const nm = escSql(r.name)
  const bid = escSql(r.batchId)
  out.push(`${stockInsertTable} (id, inventory_id, sku_id, batch_id, quantity)`)
  out.push('SELECT')
  out.push('  gen_random_uuid()::text,')
  out.push(`  '${invId}',`)
  out.push('  s.id,')
  out.push('  b.id,')
  out.push(`  ${qty}`)
  out.push('FROM "skus" s')
  out.push('INNER JOIN "batches" b')
  out.push(`  ON b.inventory_id = '${invId}' AND b."batchId" = '${bid}'`)
  out.push(`WHERE s.name = '${nm}'`)
  out.push('LIMIT 1')
  out.push('ON CONFLICT (inventory_id, sku_id, batch_id)')
  out.push(conflictUpdate)
  out.push('')
}

if (resetMissingToZero) {
  out.push('-- Set existing rows not present in the input list to zero')
  out.push('UPDATE "stocks" st')
  out.push('SET quantity = 0, last_updated = NOW()')
  out.push(`WHERE st.inventory_id = '${invId}'`)
  out.push('  AND NOT EXISTS (')
  out.push('    SELECT 1')
  out.push('    FROM "skus" s')
  out.push('    JOIN "batches" b ON b.id = st.batch_id AND b.inventory_id = st.inventory_id')
  out.push(`    WHERE s.id = st.sku_id`)
  out.push('      AND (')
  for (let i = 0; i < rows.length; i++) {
    const r = rows[i]
    const nm = escSql(r.name)
    const bid = escSql(r.batchId)
    const suffix = i === rows.length - 1 ? '' : ' OR'
    out.push(`        (s.name = '${nm}' AND b."batchId" = '${bid}')${suffix}`)
  }
  out.push('      )')
  out.push('  );')
  out.push('')
}

out.push('COMMIT;')

const sqlText = out.join('\n')
fs.writeFileSync(outPath, sqlText, 'utf8')

const outJsonPath = outPath.replace(/\.sql$/i, '.json')
const bundle = {
  kind: 'symbiotic-erp-inventory-seed',
  inventoryId: invId,
  sourceJson: sourceLabel,
  sqlFile: path.basename(outPath),
  addToExistingStock,
  resetMissingToZero,
  batchCount: uniqueBatchIds.length,
  stockRowCount: rows.length,
  notice:
    'PostgreSQL executes .sql directly (e.g. psql -f inventory_data_seed.sql). This JSON wraps the same script in the "sql" field for tooling; extract "sql" to a .sql file if your client only accepts SQL files.',
  sql: sqlText,
}
fs.writeFileSync(outJsonPath, JSON.stringify(bundle, null, 2), 'utf8')

console.log(`Wrote ${outPath}`)
console.log(`Wrote ${outJsonPath}`)
console.log(`Batches: ${uniqueBatchIds.length}, stock lines: ${rows.length}`)
console.log(`Stock on conflict: ${addToExistingStock ? 'add to existing' : 'replace'}`)
